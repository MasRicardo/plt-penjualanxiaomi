    public void showTable(String sql){
        try{
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            DefaultTableModel tableModel;
            tableModel = (DefaultTableModel)tableData.getModel();
            tableModel.getDataVector().removeAllElements();
            while(r.next()){
                tableModel.addRow(new Object[] {
                    r.getString(1), r.getString(2), r.getString(3), r.getString(4), r.getString(5), r.getString(6)
                });
                long totalIncome = 0;
                int totalHp;
                totalHp = r.getRow();
                
                for(int i = 0; i < totalHp; i++){
                    long income = Long.parseLong((String) tableModel.getValueAt(i, 3));
                    totalIncome += income;
                }
                
                totalhpTxt.setText("" + totalHp);
                totalincomeTxt.setText("" + totalIncome);
                    
            }
        } catch (SQLException ex){
            Logger.getLogger(dataform.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
