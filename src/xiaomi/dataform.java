    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        if(orderNameTxt.getText().isEmpty() && areaTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, 
                    "Masukan data yang ingin di cari", "Error", 
                    JOptionPane.ERROR_MESSAGE);
            showTable(sql);
        }else if(orderNameTxt.getText().isEmpty()){
            //kode untuk mencari berdasarkan area
            showTable("SELECT * FROM handphone WHERE tipe LIKE '%" 
                    + areaTxt.getText() + "%'");
        }else if(areaTxt.getText().isEmpty()){
            //kode mencari berdasarkan nama
            showTable("SELECT * FROM handphone WHERE name LIKE '%" 
                    + orderNameTxt.getText() + "%'");
        }else {
            //kode untuk untuk mencari berdasarkan nama dan area
            showTable("SELECT * FROM handphone WHERE name LIKE '%" 
                    + orderNameTxt.getText() + "%' AND tipe LIKE '%" 
                    + areaTxt.getText() + "%'");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

